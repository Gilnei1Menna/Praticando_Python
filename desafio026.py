frase = str('Olá, estou iniciando meus estudos de Python! ').upper().strip()
print('A letra aparece {} vezes. '.format(frase.count('O')))
print('A primeira letra aparece na posição {} da string. '.format(frase.find('O')))
print('A ultima letra aparece na posição {} da string. '.format(frase.rfind('O')))
